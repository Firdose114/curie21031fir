package curie;

public class relatio {
public static void main(String[] args) {
	int x=100;
	int z=200;
	System.out.println(x==z);
	System.out.println(x<=100);
	System.out.println(z>=200);
	System.out.println(x>z);
	System.out.println(x!=z);
	
	
	//relational operator
	System.out.println(x<=100 && z>=200);
	System.out.println(x<=100 || z>200);
	
}
}
